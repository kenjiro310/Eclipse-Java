    //function will delete user from the database
    public void deleteDrug(){
        //gets the text entered into the fields and stores them into variables
        String name = jTextField1.getText();
        String dosage = jTextField2.getText();
        
        //creates the connection variable and sets it equal to null
        Connection connect = null;
        Statement stmt = null;
        
        //need to use a try catch in oreder to connect to the database
        try{
            //class name that has the database
            Class.forName("org.sqlite.JDBC");
            //connect to the database with the path included
            connect = DriverManager.getConnection("jdbc:"
                + "sqlite:C:/Ketra/JavaApplication23/data.db");
            
            stmt = connect.createStatement();
            String sql = "DELETE from Drug where DRUGNAME= '"+name+"'";
            stmt.executeUpdate(sql);
            stmt.close();
            connect.close();
            
        }catch (Exception e){
            System.err.println( e.getClass().getName()
                    + ": " + e.getMessage() );
            System.exit(0);            
        }
        System.out.println("Opened database successfully");
    }
